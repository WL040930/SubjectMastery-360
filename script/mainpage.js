function togglepcon1() {
    var Pcon1 = document.getElementById("Pcon1");
    if (Pcon1.style.display === "none" || Pcon1.style.display === '') {
        Pcon1.style.display = "block";
    } else {
        Pcon1.style.display = "none";
    }
}

